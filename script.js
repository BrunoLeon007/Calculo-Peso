    let leituras = [];

    function adicionarLeitura() {
      const input = document.getElementById('codigoInput');
      const codigo = input.value.trim();
      const erroMsg = document.getElementById('erroMsg');
      erroMsg.textContent = '';

      if (!codigo) {
        erroMsg.textContent = "Digite um código!";
        return;
      }

      if (!(codigo in baseCodigos)) {
        erroMsg.textContent = "Código não encontrado na base!";
        input.value = '';
        input.focus();
        return;
      }

      leituras.push(codigo);
      atualizarResultados();
      input.value = '';
      input.focus();
    }

    function atualizarResultados() {
      document.getElementById('volume').textContent = leituras.length;

      let pesoTotal = leituras.reduce((soma, cod) => soma + baseCodigos[cod], 0);
      document.getElementById('pesoTotal').textContent = pesoTotal.toFixed(2);

      // Multiplicador
      const multiplicador = parseFloat(document.getElementById('multiplicador').value) || 1;
      const pesoMultiplicado = pesoTotal * multiplicador;
      document.getElementById('pesoMultiplicado').textContent = pesoMultiplicado.toFixed(2);

      // Lista de leituras
      let listaHtml = '';
      leituras.forEach((cod) => {
        listaHtml += `<span>${cod} (${baseCodigos[cod]}kg)</span><br>`;
      });
      document.getElementById('leiturasLista').innerHTML = listaHtml;
    }

    function resetar() {
      leituras = [];
      atualizarResultados();
      document.getElementById('codigoInput').value = '';
      document.getElementById('erroMsg').textContent = '';
      document.getElementById('codigoInput').focus();
    }

